inline fun String?.notEmpty(
    crossinline text: (String) -> Unit,
) {
    if (!this.isNullOrEmpty()) {
        text(this)
    }
}

inline fun String?.notEmpty(
    crossinline text: (String) -> Unit,
    crossinline empty: () -> Unit,
) {
    if (!this.isNullOrEmpty()) {
        text(this)
    } else {
        empty()
    }
}

